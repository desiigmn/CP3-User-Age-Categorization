let age = "20";

if (age < 5) {
    console.log("You're a Toddler.");
} else if (age <= 12) {
    console.log("You're a Child.");
} else if (age <= 19) {
    console.log("You're a Teenager.");
} else if (age <= 35) {
    console.log("You're a Young Adult.");
} else if (age <= 60) {
    console.log("You're Middle-Aged.");
} else {
    console.log("You're a Senior.");
}